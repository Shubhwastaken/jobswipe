create table if not exists public.realworld_students (
    student_id text primary key,
    register_number text,
    full_name text not null,
    email text,
    gender text,
    department text not null,
    "10th_marks" double precision,
    "10th_board" text,
    "12th_marks" double precision,
    "12th_board" text,
    cgpa double precision,
    backlogs_history integer default 0,
    active_backlogs integer default 0,
    year_of_study integer,
    resume_format text,
    parser_confidence text,
    synthetic_ground_truth boolean default false,
    created_at timestamptz default now()
);

create table if not exists public.realworld_skills (
    id bigint generated by default as identity primary key,
    student_id text references public.realworld_students(student_id) on delete cascade,
    skill_name text not null,
    proficiency text,
    verified boolean default false
);

create table if not exists public.realworld_certifications (
    cert_id text primary key,
    student_id text references public.realworld_students(student_id) on delete cascade,
    cert_name text not null,
    issuing_body text,
    tier text,
    domain text,
    year_obtained integer
);

create table if not exists public.realworld_projects (
    project_id text primary key,
    student_id text references public.realworld_students(student_id) on delete cascade,
    project_title text,
    domain text,
    tech_stack text,
    complexity text,
    team_size text,
    has_deployment boolean default false,
    has_github boolean default false,
    duration_weeks integer,
    year integer
);

create table if not exists public.realworld_internships (
    internship_id text primary key,
    student_id text references public.realworld_students(student_id) on delete cascade,
    company_name text,
    company_tier text,
    role text,
    domain text,
    duration_months double precision,
    stipend boolean default false,
    mode text,
    year integer
);

create table if not exists public.realworld_research_papers (
    paper_id text primary key,
    student_id text references public.realworld_students(student_id) on delete cascade,
    title text,
    publication_venue text,
    tier text,
    domain text,
    year_published integer,
    is_first_author boolean default false,
    co_authors_count integer default 0
);

create table if not exists public.realworld_companies (
    company_id text primary key,
    company_name text not null,
    industry text,
    tier text,
    min_cgpa double precision,
    min_10th double precision,
    min_12th double precision,
    max_active_backlogs integer default 0,
    allowed_departments text,
    required_skills text,
    preferred_skills text,
    min_internship_months double precision default 0,
    internship_tier_preference text default 'Any',
    min_projects integer default 0,
    project_complexity_min text default 'Basic',
    requires_research_paper boolean default false,
    cert_tier_required text default 'None',
    role_offered text,
    package_lpa double precision,
    bond_years integer default 0
);

create table if not exists public.realworld_model_runs (
    id uuid primary key default gen_random_uuid(),
    dataset_name text not null default 'resume_realworld',
    artifact_prefix text not null default 'resume_realworld',
    parser_metrics jsonb not null default '{}'::jsonb,
    model_metrics jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now()
);

create index if not exists idx_realworld_skills_student on public.realworld_skills(student_id);
create index if not exists idx_realworld_certs_student on public.realworld_certifications(student_id);
create index if not exists idx_realworld_projects_student on public.realworld_projects(student_id);
create index if not exists idx_realworld_internships_student on public.realworld_internships(student_id);
create index if not exists idx_realworld_papers_student on public.realworld_research_papers(student_id);
create index if not exists idx_realworld_students_format on public.realworld_students(resume_format);

